    private void btnIGUALActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIGUALActionPerformed
     if(!escolha.equals("")){
         b = Double.parseDouble(valor);
         double resultado = 0;
         switch (escolha){
             case "+":
                 resultado = a+b;
                 CPR.setText(String.valueOf(resultado));
                 lblMsg.setText(String.valueOf(a + "+" + b));
                 escolha = "";
                 valor = "";    
                 sPc = false;
                     break;
                     
                     case "-":
                         resultado = a-b;
                 CPR.setText(String.valueOf(resultado));
                 lblMsg.setText(String.valueOf(a + "-" + b));
                 escolha = "";
                 valor = "";    
                 sPc = false;
                     break;
                     
                     case "*":
                         resultado = a*b;
                 CPR.setText(String.valueOf(resultado));
                 lblMsg.setText(String.valueOf(a + "*" + b));
                 escolha = "";
                 valor = "";    
                 sPc = false;
                     break;
                     
                     case "/":  
                         try{
                             resultado = a/b;
                             if(String.valueOf(resultado).equalsIgnoreCase("Infinito")){
                                 throw new ArithmeticException();
                             }
                             CPR.setText(String.valueOf(resultado));
                             lblMsg.setText(String.valueOf(a + "/" + b));
                 escolha = "";
                 valor = "";    
                 sPc = false;
                         }catch (ArithmeticException e) {
                 CPR.setText("N√£o pode ser dividido por Zero");
                 escolha = "";
                 valor = "";    
                 sPc = false;
                         }
                        
                     break;
                     case "%":
                         resultado = a%b;
                 CPR.setText(String.valueOf(resultado));
                 lblMsg.setText(String.valueOf(a + "%" + b));
                 escolha = "";
                 valor = "";    
                 sPc = false;
                     break;

         }
     }
      
         
        
    }//GEN-LAST:event_btnIGUALActionPerformed

